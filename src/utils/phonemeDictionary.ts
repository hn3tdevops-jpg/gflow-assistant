export function createPlaceholder(word: string): PronunciationDictionaryEntry {
  const upper = word.toUpperCase();
  return {
    word,
    pronunciationSpelling: upper,
    phonemes: [{ token: upper, displayMode: 'simple' }],
    syllables: [{ text: word, phonemes: [{ token: upper, displayMode: 'simple' }] }],
    source: 'custom',
  };
}

export function lookupWord(
  word: string,
  customDict: Record<string, PronunciationDictionaryEntry>,
): PronunciationDictionaryEntry {
  const lower = word.toLowerCase();
  if (customDict[lower]) return customDict[lower];
  if (BUILTIN_DICTIONARY[lower]) return BUILTIN_DICTIONARY[lower];
  return createPlaceholder(word);
}
